static void test_apx_clientNodeManager_create(CuTest* tc)
{
   apx_clientNodeManager_t *manager = apx_clientNodeManager_new();
   CuAssertPtrNotNull(tc, manager);
   apx_clientNodeManager_delete(manager);
}

static void test_apx_clientNodeManager_attachLocalNode(CuTest* tc)
{
   apx_nodeData_t *node1;
   apx_nodeData_t *node2;
   apx_clientNodeManager_t *manager = apx_clientNodeManager_new();
   ApxNode_Init_TestNode1();
   node1 = ApxNode_GetNodeData_TestNode1();
   CuAssertPtrNotNull(tc, node1);
   node2 = apx_clientNodeManager_findLocalNode(manager, node1->name);
   CuAssertPtrEquals(tc, 0, node2);
   apx_clientNodeManager_attachLocalNode(manager, node1);
   node2 = apx_clientNodeManager_findLocalNode(manager, node1->name);
   CuAssertPtrEquals(tc, node1, node2);
   apx_clientNodeManager_delete(manager);
}


